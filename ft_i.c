#include "ft_printf.h"

int	ft_i(int n)
{
	int	digits;

	digits = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
		digits++;
	}
	if (n > 9)
		digits += ft_i(n / 10);
	digits++;
	n = n % 10;
	ft_putchar(n + 48);
	return (digits);
}
